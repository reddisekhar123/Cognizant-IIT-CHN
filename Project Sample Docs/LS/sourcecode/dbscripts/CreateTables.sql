CREATE TABLE ADDRESS(ADDRESS_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,ADDRESS VARCHAR(255) NOT NULL,CITY VARCHAR(255) NOT NULL,STATE VARCHAR(255) NOT NULL,COUNTRY VARCHAR(255) NOT NULL,ZIP BIGINT UNSIGNED NOT NULL,TYPE VARCHAR(255),PHONE_NUM BIGINT UNSIGNED NOT NULL);

CREATE TABLE USER_ACCOUNT(USER_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,FNAME VARCHAR(20),LNAME VARCHAR(20),DOB DATE NOT NULL,EMAIL_ID VARCHAR(255) NOT NULL,USER_NAME VARCHAR(20) NOT NULL,PASSWORD VARCHAR(20) NOT NULL,ADDRESS_ID BIGINT UNSIGNED NOT NULL,CONSTRAINT SYS_CT_5692 UNIQUE(ADDRESS_ID),CONSTRAINT SYS_CT_5693 UNIQUE(USER_NAME),CONSTRAINT FK9B07A599F92D6449 FOREIGN KEY(ADDRESS_ID) REFERENCES ADDRESS(ADDRESS_ID));

CREATE TABLE CATALOG(CATALOG_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,NAME VARCHAR(255) NOT NULL,DESCRIPTION VARCHAR(255) NOT NULL);

CREATE TABLE CATEGORY(CATEGORY_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,NAME VARCHAR(255) NOT NULL,DESCRIPTION VARCHAR(255) NOT NULL,SUB_ID BIGINT UNSIGNED, FOREIGN KEY (SUB_ID) REFERENCES CATEGORY(CATEGORY_ID));

CREATE TABLE CATALOG_CATEGORY(CATALOG_ID BIGINT UNSIGNED NOT NULL,CATEGORY_ID BIGINT UNSIGNED NOT NULL,PRIMARY KEY(CATALOG_ID,CATEGORY_ID),CONSTRAINT FKB8AA5A8490F7B629 FOREIGN KEY(CATALOG_ID) REFERENCES CATALOG(CATALOG_ID));

CREATE TABLE CREDITCARD(CARD_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,CARD_NUMBER BIGINT(20) UNSIGNED NOT NULL,CARD_TYPE VARCHAR(255) NOT NULL,EXP_DATE DATE NOT NULL);

CREATE TABLE PRODUCT(PRODUCT_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,NAME VARCHAR(255) NOT NULL,DESCRIPTION VARCHAR(255) NOT NULL,COST DOUBLE UNSIGNED,UNITS BIGINT UNSIGNED);

CREATE TABLE CATEGORY_PRODUCT(CATEGORY_ID BIGINT UNSIGNED NOT NULL,PRODUCT_ID BIGINT UNSIGNED NOT NULL,PRIMARY KEY(CATEGORY_ID,PRODUCT_ID),CONSTRAINT FKC50BCE8E25E163CB FOREIGN KEY(CATEGORY_ID) REFERENCES CATEGORY(CATEGORY_ID));

CREATE TABLE ITEM(ITEM_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,ITEM_NAME VARCHAR(255) NOT NULL,DESCRIPTION VARCHAR(255) NOT NULL,PRICE DOUBLE UNSIGNED NOT NULL,PRODUCT_ID BIGINT(20) UNSIGNED,FOREIGN KEY(PRODUCT_ID) REFERENCES PRODUCT(PRODUCT_ID));

CREATE TABLE ORDER_ITEM(ORDER_ID BIGINT UNSIGNED NOT NULL,ITEM_ID BIGINT UNSIGNED,O_I_ID INTEGER UNSIGNED,PRIMARY KEY(ORDER_ID,O_I_ID));

CREATE TABLE ORDER_PRODUCT(ORDER_ID BIGINT UNSIGNED NOT NULL,QUANTITY BIGINT UNSIGNED,PRODUCT_ID BIGINT UNSIGNED,O_P_ID INTEGER UNSIGNED,PRIMARY KEY(ORDER_ID,O_P_ID));

CREATE TABLE IMAGE(IMAGE_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,NAME VARCHAR(255) NOT NULL,PATH VARCHAR(255) NOT NULL,ITEM_ID BIGINT UNSIGNED,PRODUCT_ID BIGINT UNSIGNED,CATEGORY_ID BIGINT UNSIGNED,CATALOG_ID BIGINT UNSIGNED,FOREIGN KEY(CATEGORY_ID) REFERENCES CATEGORY(CATEGORY_ID),FOREIGN KEY(CATALOG_ID) REFERENCES CATALOG(CATALOG_ID),FOREIGN KEY(PRODUCT_ID) REFERENCES PRODUCT(PRODUCT_ID),FOREIGN KEY(ITEM_ID) REFERENCES ITEM(ITEM_ID));

CREATE TABLE TORDER(ORDER_ID BIGINT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,ORDERSTATUS VARCHAR(255) NOT NULL,DATE DATE NOT NULL,USER_ID BIGINT UNSIGNED NOT NULL,CREDIT_CARD_ID BIGINT UNSIGNED NOT NULL,ADDRESS_ID BIGINT UNSIGNED NOT NULL,CONSTRAINT SYS_CT_5686 UNIQUE(ADDRESS_ID),CONSTRAINT SYS_CT_5687 UNIQUE(CREDIT_CARD_ID),CONSTRAINT FK93D6A35AF92D6449 FOREIGN KEY(ADDRESS_ID) REFERENCES ADDRESS(ADDRESS_ID),CONSTRAINT FK93D6A35A8037A59E FOREIGN KEY(CREDIT_CARD_ID) REFERENCES CREDITCARD(CARD_ID));


